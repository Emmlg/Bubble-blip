    public void fill(int tiempo) {

    Thread t;
        t = new Thread(new Runnable() {
            @Override
            public void run(){
                try{
                    int counter = 0;
                    while(counter<=100) {
                        
                        barra.setValue(counter);
                        
                        Thread.sleep(tiempo);
                        counter +=2;
                        // System.out.println(pg.getValue());
                    }
                    
                    VentanaSiguiente();
                
                }catch(Exception ae){
                    ae.printStackTrace(); 
                }
            }
        });
    t.start();
       
}
    

